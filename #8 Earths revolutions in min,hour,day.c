#include <stdio.h>
int main()
{
    float rev_seconds,rev_year,rev_hour,rev_min;
    rev_seconds = 31558150;
    rev_year = rev_seconds/(60*60*24*365);
    rev_hour = rev_seconds/3600;
    rev_min = rev_seconds/60;
    printf(" Revolutions in year  is %f \n",rev_year);
    printf("Revolutions in hours is %f \n",rev_hour);
    printf("Revolutions in minutes is %f \n",rev_min);
    return 0;
}
